import (
	"regexp"
	"strings"
)

func isValid(s string) bool {
	for strings.Contains(s, "()") || strings.Contains(s, "[]") || strings.Contains(s, "{}") {
		re, _ := regexp.Compile(`\(\)|\[\]|\{\}`)
		s = re.ReplaceAllString(s, "")
	}

	if s == "" {
		return true
	}
	return false
}
